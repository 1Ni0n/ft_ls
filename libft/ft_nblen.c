#include "libft.h"
#include <stdio.h>

size_t	ft_nblen(long nb)
{
	int		len;

	len = 0;
	if (nb < 0)
		len++;
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
